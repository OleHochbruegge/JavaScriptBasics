/**
 * Declaring a number variable is the same a string value except we do not need the '' when assigning a value.
 */

// Declaring a num variable with the value 503
let num = 503
// Print it
console.log(num)

// We can use any of the math operators will declaring a number variable.
num = 503 - 3
// Print it 
console.log(num)

// Using + operator 
num = 500 + 3
// Print it 
console.log(num)

// Using the / operator
num = 8 / 2
// Print it 
console.log(num)

// Using the * operator
num = 5 * 100
// Print it 
console.log(num)

// Using the % Operator
num = 9 % 2
// Print it 
console.log(num)
